//----------------------------------MATH METHODS-------------------------------------------------





console.log("Math.round(1.2) : ", Math.round(1.2)); // Returns x rounded to its nearest integer
console.log("Math.round(1.5) : ", Math.round(1.5)); // Returns x rounded to its nearest integer
console.log("Math.round(1.7) : ", Math.round(1.7)); // Returns x rounded to its nearest integer





console.log("Math.ceil(1.2) : ", Math.ceil(1.2)); // Returns x rounded up to its nearest integer
console.log("Math.ceil(1.5) : ", Math.ceil(1.5)); // Returns x rounded up to its nearest integer
console.log("Math.ceil(1.7) : ", Math.ceil(1.7)); // Returns x rounded up to its nearest integer




console.log("Math.floor(1.2) : ", Math.floor(1.2)); // Returns x rounded down to its nearest integer
console.log("Math.floor(1.5) : ", Math.floor(1.5)); // Returns x rounded down to its nearest integer
console.log("Math.floor(1.7) : ", Math.floor(1.7)); // Returns x rounded down to its nearest integer



console.log("Math.trunc(1.2) : ", Math.trunc(1.2)); // Returns the integer part of x
console.log("Math.trunc(1.5) : ", Math.trunc(1.5)); // Returns the integer part of x
console.log("Math.trunc(1.7) : ", Math.trunc(1.7)); // Returns the integer part of x




